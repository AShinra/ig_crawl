options.add_argument('--blink-settings=imagesEnabled=false')
driver = webdriver.Chrome(options=options)

driver.get('https://starngage.com/app/global/influencer/ranking/philippines')

elements = driver.find_elements(By.XPATH, '//td[@class="align-middle text-break"]')

influencers = {}

for element in elements:
    ele = element.text
    ele = ele.split('\n')
    influencers[ele[0]] = ele[-1]
    print(ele)

